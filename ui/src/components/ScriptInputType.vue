<template>
  <div>
    <b-form-group label="Data type"
                  label-for="input-type-selection">
      <b-card no-body>
        <b-tabs card
                active-nav-item-class=""
                active-tab-class="">
          <b-tab title="String" active>
            <ScriptInputTypeString />
          </b-tab>
          <b-tab title="Number">
            <ScriptInputTypeNumber />
          </b-tab>
          <b-tab title="Boolean">
            <ScriptInputTypeBoolean />
          </b-tab>
          <b-tab title="Other">
            <ScriptInputTypeOther />
          </b-tab>
        </b-tabs>
      </b-card>
    </b-form-group>
  </div>
</template>

<script>
import { store } from '../store.js'

import ScriptInputTypeNumber from '@/components/ScriptInputTypeNumber.vue'
import ScriptInputTypeString from '@/components/ScriptInputTypeString.vue'
import ScriptInputTypeBoolean from '@/components/ScriptInputTypeBoolean.vue'
import ScriptInputTypeOther from '@/components/ScriptInputTypeOther.vue'

// import { ValidationProvider } from 'vee-validate'

export default {
  name: 'ScriptInputType',
  components: {
    ScriptInputTypeNumber,
    ScriptInputTypeBoolean,
    ScriptInputTypeOther,
    ScriptInputTypeString
    // ValidationProvider
  },
  props: {
    // Script input id for this type component.
    id: {
      required: false,
      type: Number,
      default: -1
    }
  },
  created () {
    this.thisScriptInput = store.getScriptInputById(this.id)
  },
  data () {
    return {
      thisScriptInput: {},
      dataType: '',
      dataSubtype: '',
      defaultValue: '',
      isValueRequired: false,
      selected: 'plain-string',
      stringTypeSelected: false,
      numberTypeSelected: false,
      booleanTypeSelected: false,
      otherTypeSelected: false,
      signed: false,
      stringRequired: false,
      numericRequired: false,
      booleanRequired: false,
      otherRequired: false,
      stringDefault: '',
      numericDefault: '',
      booleanDefault: '',
      otherDefault: '',
      numericAtMost: '',
      numericAtLeast: ''
    }
  },
  methods: {
    typeSelected: function () {
      this.unselectAll()
    },
    unselectAll: function () {
      this.stringTypeSelected = false
      this.numberTypeSelected = false
      this.booleanTypeSelected = false
      this.otherTypeSelected = false
    }
  }
}
</script>

